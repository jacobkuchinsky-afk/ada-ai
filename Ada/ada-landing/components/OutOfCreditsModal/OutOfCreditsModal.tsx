'use client';

import { useEffect } from 'react';
import Link from 'next/link';
import styles from './OutOfCreditsModal.module.css';

interface OutOfCreditsModalProps {
  isOpen: boolean;
  onClose: () => void;
}

export default function OutOfCreditsModal({ isOpen, onClose }: OutOfCreditsModalProps) {
  // Close on escape key
  useEffect(() => {
    const handleEscape = (e: KeyboardEvent) => {
      if (e.key === 'Escape') {
        onClose();
      }
    };

    if (isOpen) {
      document.addEventListener('keydown', handleEscape);
      document.body.style.overflow = 'hidden';
    }

    return () => {
      document.removeEventListener('keydown', handleEscape);
      document.body.style.overflow = '';
    };
  }, [isOpen, onClose]);

  if (!isOpen) return null;

  return (
    <div className={styles.overlay} onClick={onClose}>
      <div className={styles.modal} onClick={(e) => e.stopPropagation()}>
        <button className={styles.closeButton} onClick={onClose}>
          <svg
            width="20"
            height="20"
            viewBox="0 0 24 24"
            fill="none"
            stroke="currentColor"
            strokeWidth="2"
            strokeLinecap="round"
            strokeLinejoin="round"
          >
            <line x1="18" y1="6" x2="6" y2="18" />
            <line x1="6" y1="6" x2="18" y2="18" />
          </svg>
        </button>

        <div className={styles.iconContainer}>
          <svg
            width="48"
            height="48"
            viewBox="0 0 24 24"
            fill="none"
            stroke="currentColor"
            strokeWidth="1.5"
            strokeLinecap="round"
            strokeLinejoin="round"
          >
            <circle cx="12" cy="12" r="10" />
            <line x1="12" y1="8" x2="12" y2="12" />
            <line x1="12" y1="16" x2="12.01" y2="16" />
          </svg>
        </div>

        <h2 className={styles.title}>Out of Credits</h2>
        <p className={styles.message}>
          You are out of credits upgrade to premium for more
        </p>

        <div className={styles.actions}>
          <Link href="/profile" className={styles.upgradeButton}>
            Upgrade to Premium
          </Link>
          <button className={styles.dismissButton} onClick={onClose}>
            Maybe Later
          </button>
        </div>
      </div>
    </div>
  );
}


